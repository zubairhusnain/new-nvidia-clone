<div class="nv-text text aem-GridColumn--offset--tablet--0 aem-GridColumn--default--none aem-GridColumn--phone--none aem-GridColumn--phone--12 aem-GridColumn--tablet--none aem-GridColumn aem-GridColumn--default--7 aem-GridColumn--tablet--8 aem-GridColumn--offset--phone--0 aem-GridColumn--offset--default--0">
 
 	
    <div id="nv-text-361c53beb7" class="general-container-text            ">
			<div class="text-left lap-text-left tab-text-left mob-text-left">
				<div class="description ">
                	<p>Digital twins are born by integrating data that best describes their real-world counterparts. These data sources and formats vary based on the type of digital twin, industry, and use case, but are typically composed of 1D (e.g., tabular data from IT/OT systems) and 2D/3D (e.g., CAD, reality capture scans, BIM) data. Combining these data sources to create digital twins unlocks incredible new possibilities, from advanced design and planning to simulation (using <a href="/glossary/simready/">SimReady</a> assets) and remote monitoring and control of operations. Internet of Things (IoT) sensors and devices play a crucial role in providing real-time data that keeps digital twins accurate and up-to-date, allowing for dynamic interactions between the physical and digital realms.</p>
